    public static double factorial_1(int[] vector){
        double resultado=1;
        for(int i = 1 ; i <= vector[0] ; i=i+1)
            resultado=resultado*i;
        
        return resultado;
    }
    public static double factorial_2(int[] vector){
        double resultado=1;
        for(int i = 1 ; i <= vector[0]+vector[2] ; i=i+1)
            resultado=resultado*i;
        
        return resultado;
    } 
    public static double exponente(int[] vector){
        double total=1;
        for(int i = 1 ; i <= vector[1] ; i=i+1){
            total = total * vector[2];
        }
        return total ;
    }
